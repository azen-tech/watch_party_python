
# Integers
age = 30
print(age)
print(type(age))

# Floats
height = 1.75
print(height)
print(type(height))

# Strings
name = "John"
print(name)
print(type(name))

# Booleans
is_tall = True
print(is_tall)
print(type(is_tall))

# Lists
fruits = ["apple", "banana", "cherry"]
print(fruits)
print(type(fruits))

# Tuples
coordinates = (10, 20)
print(coordinates)
print(type(coordinates))

# Dictionaries
person = {"name": "John", "age": 30, "is_tall": True}
print(person)
print(type(person))
